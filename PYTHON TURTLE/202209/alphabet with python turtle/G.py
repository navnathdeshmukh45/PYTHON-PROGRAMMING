#.....draw letter G with python turtle........!
import turtle
t = turtle.Turtle()
t.penup()
#draw straight line
t.goto(-30, 50)  # centering in the screen
t.pendown()
t.pensize(10)
t.pencolor("red")
t.right(180)
t.circle(50, 180)
t.left(90)
t.forward(50)
t.goto(-50, 0)
t.right(90)
t.forward(50)
t.right(90)
t.forward(20)
