#.....draw letter Y with python turtle........!
import turtle
t=turtle.Turtle()
t.penup()
t.goto(-30,50) #centering in the screen
t.pendown()
t.pensize(10)
t.pencolor("red")
t.right(65)
t.forward(100)
t.left(130)
t.forward(100)
 
t.penup()
t.goto(13,-43)
t.left(25)
t.pendown()
t.backward(100)