#.....draw letter W with python turtle........!
import turtle
t=turtle.Turtle()
t.penup()
t.goto(-30,50) #centering in the screen
t.pendown()
t.pensize(10)
t.pencolor("red")
t.right(65)
t.forward(100)
t.left(130)
t.forward(100)
 
t.right(120)
t.forward(100)
t.left(130)
t.forward(100)